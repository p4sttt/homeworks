from fnmatch import fnmatch

for n in range(123450608, 123459698):
    if n % 17 == 0 and fnmatch(str(n), "12345?6?8"):
        print(n, int(n / 17))
# 123450668 7261804
# 123451688 7261864
# 123456618 7262154
# 123457638 7262214
# 123458658 7262274
# 123459678 7262334

for n in range(1245, 10 ** 6):
    if n % 51 == 0 and fnmatch(str(n), "12*45*"):
        print(n, int(n / 51))
# 123450668 7261804
# 123451688 7261864
# 123456618 7262154
